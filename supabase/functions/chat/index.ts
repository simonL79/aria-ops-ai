import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are A.R.I.A™ — Adaptive Reputation Intelligence & Analysis. You are the AI assistant on the A.R.I.A website (ariaops.co.uk).

Your role:
- Answer questions about A.R.I.A's reputation management services
- Qualify leads by understanding their situation
- Be professional, confident, and concise
- Encourage visitors to request a private scan or fill out the contact form

Services you know about:
1. Social Media Protection — monitoring Twitter, Instagram, LinkedIn, Facebook for threats
2. Influence & Creator Shield — monitoring influencer mentions, creator network management
3. Executive Protection — high-level threat identification, personal brand protection
4. Dark Web Lead Monitoring — alerts from underground forums
5. AI Initiative Watching — tracking AI models trained on negative content
6. Family Reputation Package — protection for family members
7. Full Service Takeovers — end-to-end reputation defense

Key stats: 500+ threats neutralized, 98% client retention, 24/7 monitoring, <2hr avg response time.

Keep responses short (2-4 sentences). Be helpful but steer toward booking a consultation. Never reveal pricing — say "pricing is tailored to each client's needs" and encourage them to request an assessment.`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    if (!messages || !Array.isArray(messages)) {
      return new Response(JSON.stringify({ error: "messages array required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [{ role: "system", content: SYSTEM_PROMPT }, ...messages],
        stream: true,
      }),
    });

    if (!response.ok) {
      const status = response.status;
      if (status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited. Please try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", status, t);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
